import React, { useState } from "react";
import TodoForm from "./TodoForm";
import Todo from "./Todo";

function TodoList() {
  const [todos, setTodos] = useState([]);

  const addTodo = todo => {
    if (!todo.text || /^\s*$/.test(todo.text)) {
      //Here ^ means the beginning of expression. \s* means 0 or more occurrences of space characters(' ', tab etc). $ means end of the string. So /^\s*$/ is the regex for empty string or string with only spaces.
      return;
    }

    const newTodos = [todo, ...todos];

    setTodos(newTodos);
  };

  const updateTodo = (todoId, newValue) => {
    if (!newValue.text || /^\s*$/.test(newValue.text)) {
      //Here ^ means the beginning of expression. \s* means 0 or more occurrences of space characters(' ', tab etc). $ means end of the string. So /^\s*$/ is the regex for empty string or string with only spaces.
      return;
    }

    setTodos(prev => prev.map(item => (item.id === todoId ? newValue : item)));
  };

  const removeTodo = id => {
    const removedArr = [...todos].filter(todo => todo.id !== id);
    setTodos(removedArr);
  };

  const completeTodo = id => {
    let updatedTodos = todos.map(todo => {
      if (todo.id === id) {
        todo.isComplete = !todo.isComplete;
      }
      return todo;
    });
    setTodos(updatedTodos);
  };

  return (
    <div>
      <h1>Quais seus planos para hoje?</h1>
      <TodoForm
        onSubmit={addTodo}
        placeholder="Liste aqui o que fazer!"
        btnText="Adicionar"
      />

      <Todo
        todos={todos}
        completeTodo={completeTodo}
        removeTodo={removeTodo}
        updateTodo={updateTodo}
      />
    </div>
  );
}

export default TodoList;
